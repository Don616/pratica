public class Aula3 {
	public static void main(String[] args){
		System.out.println("Switch: ");
		int num = 5;

		switch (num) {
			case 1:
				System.out.println(num);
				break;
			case 2:
				System.out.println(num);
				break;
			case 5:
				System.out.println(num);
				break;
			default:
				System.out.println(num);
				break;

		}

	}
}
